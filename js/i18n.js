/**
 * Internationalization (i18n) module
 * Supports DE (default), FR, IT, EN via ?lang= URL parameter
 */

export const SUPPORTED_LANGS = ["de", "fr", "it", "en"];
const DEFAULT_LANG = "de";
let currentLang = DEFAULT_LANG;

/** Get current language */
export function getLang() {
  return currentLang;
}

/** Get locale string for number formatting */
export function getLocale() {
  const map = { de: "de-CH", fr: "fr-CH", it: "it-CH", en: "en-CH" };
  return map[currentLang] || "de-CH";
}

/** Initialize language from URL parameter */
export function initLang() {
  const param = new URLSearchParams(window.location.search).get("lang");
  currentLang = SUPPORTED_LANGS.includes(param) ? param : DEFAULT_LANG;
  document.documentElement.lang = currentLang;
  const select = document.getElementById("lang-select");
  if (select) select.value = currentLang;
  translatePage();
  return currentLang;
}

/** Change language, update URL, re-translate page */
export function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  document.documentElement.lang = lang;

  // Update URL — preserve other params
  const params = new URLSearchParams(window.location.search);
  if (lang === DEFAULT_LANG) params.delete("lang");
  else params.set("lang", lang);
  const qs = params.toString();
  const url = window.location.pathname + (qs ? "?" + qs : "");
  window.history.replaceState(null, "", url);

  const select = document.getElementById("lang-select");
  if (select) select.value = lang;

  translatePage();
  window.dispatchEvent(new Event("langchange"));
}

/** Translate a key with optional interpolation: t("key", { var: value }) */
export function t(key, params) {
  const str = translations[currentLang]?.[key] ?? translations.de[key] ?? key;
  if (!params) return str;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`);
}

/** Update all elements with data-i18n attributes */
export function translatePage() {
  document.title = t("meta.title");
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
}

